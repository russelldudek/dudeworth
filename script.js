const get = (obj, path) => path.split(".").reduce((value, key) => value?.[key], obj);

const setText = (storyboard) => {
  document.querySelectorAll("[data-story]").forEach((node) => {
    const value = get(storyboard, node.dataset.story);
    if (typeof value !== "string") return;
    if (node.dataset.accent) {
      const accent = node.dataset.accent;
      const parts = value.split(accent);
      node.replaceChildren();
      parts.forEach((part, index) => {
        if (part) node.append(document.createTextNode(part));
        if (index < parts.length - 1) {
          const span = document.createElement("span");
          span.className = "text-accent";
          span.textContent = accent;
          node.append(span);
        }
      });
      return;
    }
    node.textContent = value;
  });
};

